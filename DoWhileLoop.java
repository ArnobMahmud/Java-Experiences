package main;
import java.util.Scanner;

public class DoWhileLoop {
	
	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		
		int i;
		
		i = 1;
		do {
			System.out.println("***");
			System.out.println("**");
			System.out.println("*");
			i++;
		} while(i<=10);
		
	}

}
