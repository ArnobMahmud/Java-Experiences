package w3Resources;

public class arrays {
    public static void main(String[] args) {
        String[] cars = { "Volvo", "BMW", "Ford", "Mazda" };
        System.out.println(cars[0]);
        // Outputs Volvo

        
    // To find out how many elements an array has, use the length property:
        System.out.println(cars.length);
        // Outputs 4
    }
}


